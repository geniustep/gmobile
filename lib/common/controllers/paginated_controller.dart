            if (kDebugMode) {
              print('ğŸ No more items to load');
            }
          }

          _items.addAll(newItems);
          _currentPage++;

          if (kDebugMode) {
            print('ğŸ“Š Total items: ${_items.length}');
          }
        },
        error: (err) {
          if (kDebugMode) {
            print('âŒ Error loading items: ${err.message}');
          }
          _error.value = err;
        },
        loading: () {
