        print('ğŸ“¥ Loading page $_currentPage (offset: ${_currentPage * pageSize}, limit: $pageSize)');
      }

      final result = await fetcher(pageSize, _currentPage * pageSize);

      result.when(
        success: (newItems) {
          if (kDebugMode) {
            print('âœ… Loaded ${newItems.length} items');
          }

          // Ø¥Ø°Ø§ Ø¹Ø¯Ø¯ Ø§Ù„Ø¹Ù†Ø§ØµØ± Ø£Ù‚Ù„ Ù…Ù† pageSizeØŒ Ù„Ø§ ÙŠÙˆØ¬Ø¯ Ø§Ù„Ù…Ø²ÙŠØ¯
          if (newItems.length < pageSize) {
